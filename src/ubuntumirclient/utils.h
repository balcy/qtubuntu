#ifndef UTILS_H
#define UTILS_H

#include <QtGlobal>

namespace {
    inline int divideAndRoundUp(int numerator, qreal denominator)
    {
        return ceil((qreal)numerator / denominator);
    }
} // anonymous namespace

#endif // UTILS_H
